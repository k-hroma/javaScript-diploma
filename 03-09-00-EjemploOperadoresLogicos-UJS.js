// Operadore Logicos
let a = false;
let b = false;
console.log(a);
console.log(b);

// Operador Logico && (and o Y)
// Regresa verdadero si ambos operandos
// son verdaderos
console.log(`${a} && ${b} -> ${a && b}`);

// Operador Logico || (or o O)
// Regresa verdadero si cualquiera de los valores es verdadero
console.log(`${a} || ${b} -> ${a || b}`);

// Operador ! (not o NO)
// Invierte el valor original, true -> false, false -> true
console.log(`${a} -> ${!a}`);
