// Tipos de datos primitivos

//Number -> almacena valores numéricos: 10, -40, 3.9, etc

let miEntero = 10
console.log(miEntero)
console.log(typeof miEntero)
// -> let puede almacenar cualquier tipo de dato
miEntero = "hola"
console.log(miEntero)
console.log(typeof miEntero)
//String -> almacena cadenas de caractéres: "hola", 'hola', `hola`

let miCadena = "puede ir en comillas o ('') o (``) "
console.log(miCadena)
console.log(typeof miCadena)
//Boolean -> almacena un valor lógico: true o false

let miBoolean = true
console.log(miBoolean)
console.log(typeof miBoolean)

//Null -> ausencia de la referencia de un objeto: null

let miNull = null
console.log(miNull) 
console.log(typeof miNull)
//Undefined -> ausencia de valor: undefined

let miUndefined = undefined
console.log(miUndefined)
console.log(typeof miUndefined)

